def generate_pdf(scan_id: str, domain: str, findings: list[dict], scan_started: str) -> bytes:
    """Generate a professional PDF report and return it as bytes."""
    buf = io.BytesIO()
    doc = SimpleDocTemplate(
        buf,
        pagesize=A4,
        leftMargin=2 * cm,
        rightMargin=2 * cm,
        topMargin=2 * cm,
        bottomMargin=2 * cm,
        title=f"Bug Bounty Report — {domain}",
        author="BugHunter Pro",
    )

    styles = _build_styles()
    story = []

    # ── Cover page ────────────────────────────────────────────────────────────
    story += _cover_page(domain, scan_id, scan_started, findings, styles)
    story.append(Spacer(1, 1 * cm))

    # ── Executive summary ─────────────────────────────────────────────────────
    story += _executive_summary(domain, findings, styles)
    story.append(Spacer(1, 0.5 * cm))

    # ── Summary table ─────────────────────────────────────────────────────────
    story += _summary_table(findings, styles)
    story.append(Spacer(1, 1 * cm))

    # ── Findings ──────────────────────────────────────────────────────────────
    story.append(Paragraph("Detailed Findings", styles["h1"]))
    story.append(HRFlowable(width="100%", thickness=1, color=ACCENT))
    story.append(Spacer(1, 0.5 * cm))

    severity_order = ["critical", "high", "medium", "low", "info"]
    sorted_findings = sorted(
        findings,
        key=lambda f: severity_order.index(f.get("severity", "info")) if f.get("severity", "info") in severity_order else 99,
    )

    for i, finding in enumerate(sorted_findings, 1):
        story += _finding_block(i, finding, styles)
        story.append(Spacer(1, 0.8 * cm))

    # ── Disclaimer ────────────────────────────────────────────────────────────
    story.append(HRFlowable(width="100%", thickness=1, color=colors.HexColor("#333")))
    story.append(Spacer(1, 0.3 * cm))
    story.append(Paragraph(
        "This report was generated by BugHunter Pro for authorized security testing only. "
        "Unauthorized use of this tool or the techniques described herein may violate the "
        "Computer Misuse Act 1990 and equivalent legislation. Always obtain explicit written "
        "permission before testing any system you do not own.",
        styles["disclaimer"],
    ))

    doc.build(story)
    return buf.getvalue()


