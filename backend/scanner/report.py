def _cover_page(domain, scan_id, scan_started, findings, styles):
    counts = _count_by_severity(findings)

    elements = [
        Spacer(1, 2 * cm),
        Paragraph("BUG BOUNTY REPORT", styles["cover_title"]),
        Spacer(1, 0.3 * cm),
        Paragraph(domain, styles["cover_domain"]),
        Spacer(1, 1 * cm),
        HRFlowable(width="100%", thickness=2, color=ACCENT),
        Spacer(1, 0.8 * cm),
        Paragraph(f"Scan ID: {scan_id}", styles["meta"]),
        Paragraph(f"Date: {scan_started}", styles["meta"]),
        Paragraph(f"Generated: {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}", styles["meta"]),
        Spacer(1, 1.5 * cm),
        Paragraph("Vulnerability Summary", styles["h2"]),
        Spacer(1, 0.4 * cm),
    ]

    # Severity badge table
    badge_data = [[
        Paragraph(f"<b>{counts.get(s, 0)}</b><br/><font size='8'>{s.upper()}</font>", styles["badge_text"])
        for s in ["critical", "high", "medium", "low", "info"]
    ]]
    badge_table = Table(badge_data, colWidths=[3.2 * cm] * 5, rowHeights=[1.4 * cm])
    badge_table.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (0, 0), SEVERITY_CONFIG["critical"]["color"]),
        ("BACKGROUND", (1, 0), (1, 0), SEVERITY_CONFIG["high"]["color"]),
        ("BACKGROUND", (2, 0), (2, 0), SEVERITY_CONFIG["medium"]["color"]),
        ("BACKGROUND", (3, 0), (3, 0), SEVERITY_CONFIG["low"]["color"]),
        ("BACKGROUND", (4, 0), (4, 0), SEVERITY_CONFIG["info"]["color"]),
        ("ALIGN", (0, 0), (-1, -1), "CENTER"),
        ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
        ("TEXTCOLOR", (0, 0), (-1, -1), WHITE),
        ("FONTSIZE", (0, 0), (-1, -1), 16),
        ("FONTNAME", (0, 0), (-1, -1), "Helvetica-Bold"),
        ("ROUNDEDCORNERS", [4, 4, 4, 4]),
        ("LEFTPADDING", (0, 0), (-1, -1), 6),
        ("RIGHTPADDING", (0, 0), (-1, -1), 6),
    ]))
    elements.append(badge_table)

    return elements


def _executive_summary(domain, findings, styles):
    counts = _count_by_severity(findings)
    total = len(findings)
    critical = counts.get("critical", 0)
    high = counts.get("high", 0)

    risk = "CRITICAL" if critical > 0 else ("HIGH" if high > 0 else "MEDIUM" if counts.get("medium", 0) > 0 else "LOW")

    summary_text = (
        f"A security assessment of <b>{domain}</b> identified <b>{total}</b> vulnerabilities across multiple attack categories. "
    )
    if critical:
        summary_text += f"<b>{critical} critical-severity</b> issue(s) require immediate remediation as they may allow full system compromise or sensitive data exposure. "
    if high:
        summary_text += f"<b>{high} high-severity</b> issue(s) pose significant risk and should be addressed as a priority. "
    summary_text += (
        "This report provides detailed reproduction steps, business impact analysis, "
        "and specific remediation guidance for each finding."
    )

    return [
        Paragraph("Executive Summary", styles["h1"]),
        HRFlowable(width="100%", thickness=1, color=ACCENT),
        Spacer(1, 0.4 * cm),
        Paragraph(summary_text, styles["body"]),
        Spacer(1, 0.3 * cm),
        Paragraph(f"Overall Risk Rating: <b>{risk}</b>", styles["risk_" + risk.lower()] if f"risk_{risk.lower()}" in styles else styles["body"]),
    ]


def _summary_table(findings, styles):
    counts = _count_by_severity(findings)
    cat_counts = {}
    for f in findings:
        cat = f.get("type", "other").replace("_", " ").title()
        cat_counts[cat] = cat_counts.get(cat, 0) + 1

    header = [Paragraph("<b>Category</b>", styles["table_header"]),
              Paragraph("<b>Count</b>", styles["table_header"]),
              Paragraph("<b>Severity</b>", styles["table_header"])]
    rows = [header]

    severity_for_type = {
        "Xss": "High", "Sqli": "Critical", "Ssrf": "Critical",
        "Cors": "High", "Redirect": "Medium", "Header": "Low–Medium",
        "Exposed File": "Critical–Medium", "Secret In Js": "Critical–High",
        "Takeover": "High", "Subdomain": "Info", "Open Port": "Low",
        "Admin Panel": "Medium",
    }
    for cat, cnt in sorted(cat_counts.items(), key=lambda x: x[1], reverse=True):
        rows.append([
            Paragraph(cat, styles["table_cell"]),
            Paragraph(str(cnt), styles["table_cell"]),
            Paragraph(severity_for_type.get(cat, "—"), styles["table_cell"]),
        ])

    t = Table(rows, colWidths=[9 * cm, 3 * cm, 5 * cm])
    t.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, 0), ACCENT),
        ("TEXTCOLOR", (0, 0), (-1, 0), WHITE),
        ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.HexColor("#1a1a2e"), colors.HexColor("#16213e")]),
        ("TEXTCOLOR", (0, 1), (-1, -1), BODY_TXT),
        ("GRID", (0, 0), (-1, -1), 0.5, colors.HexColor("#333")),
        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
        ("FONTSIZE", (0, 0), (-1, -1), 9),
        ("TOPPADDING", (0, 0), (-1, -1), 6),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
        ("LEFTPADDING", (0, 0), (-1, -1), 8),
    ]))

    return [
        Paragraph("Finding Categories", styles["h2"]),
        Spacer(1, 0.3 * cm),
        t,
    ]


def _finding_block(num: int, finding: dict, styles) -> list:
    severity = finding.get("severity", "info")
    sev_color = SEVERITY_CONFIG.get(severity, SEVERITY_CONFIG["info"])["color"]
    sev_label = SEVERITY_CONFIG.get(severity, SEVERITY_CONFIG["info"])["label"]
    cvss_range = SEVERITY_CONFIG.get(severity, SEVERITY_CONFIG["info"])["cvss_range"]
    ftype = finding.get("type", "other")
    refs = REFERENCES.get(ftype, [])

    elements = []

    # Finding header row
    header_data = [[
        Paragraph(f"<b>#{num}</b>", styles["finding_num"]),
        Paragraph(finding.get("title", "Unknown Finding"), styles["finding_title"]),
        Paragraph(sev_label, styles["sev_badge"]),
    ]]
    header_table = Table(header_data, colWidths=[1 * cm, 13.5 * cm, 2.5 * cm])
    header_table.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, -1), colors.HexColor("#1a1a2e")),
        ("LEFTPADDING", (0, 0), (-1, -1), 8),
        ("RIGHTPADDING", (0, 0), (-1, -1), 8),
        ("TOPPADDING", (0, 0), (-1, -1), 8),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
        ("BACKGROUND", (2, 0), (2, 0), sev_color),
        ("TEXTCOLOR", (2, 0), (2, 0), WHITE),
        ("ALIGN", (2, 0), (2, 0), "CENTER"),
        ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
    ]))
    elements.append(header_table)

    # Details
    url = finding.get("url", finding.get("evidence", {}).get("url", "N/A"))
    evidence = finding.get("evidence", {})

    detail_rows = [
        [Paragraph("<b>URL</b>",          styles["detail_label"]), Paragraph(_safe(url),                     styles["detail_value"])],
        [Paragraph("<b>Type</b>",         styles["detail_label"]), Paragraph(_safe(ftype.replace("_", " ").title()),styles["detail_value"])],
        [Paragraph("<b>CVSS Range</b>",   styles["detail_label"]), Paragraph(_safe(cvss_range),              styles["detail_value"])],
        [Paragraph("<b>Description</b>",  styles["detail_label"]), Paragraph(_safe(finding.get("description", "")), styles["detail_value"])],
        [Paragraph("<b>Impact</b>",       styles["detail_label"]), Paragraph(_impact_for(ftype),             styles["detail_value"])],
        [Paragraph("<b>Remediation</b>",  styles["detail_label"]), Paragraph(_safe(finding.get("remediation", "")), styles["detail_value"])],
    ]

    if evidence:
        evidence_str = "\n".join(f"{k}: {str(v)[:200]}" for k, v in evidence.items() if v)
        detail_rows.append([
            Paragraph("<b>Evidence</b>", styles["detail_label"]),
            Paragraph(_safe(evidence_str[:600]), styles["code"]),
        ])

    if refs:
        detail_rows.append([
            Paragraph("<b>References</b>", styles["detail_label"]),
            Paragraph("  •  ".join(_safe(r) for r in refs), styles["detail_value"]),
        ])

    detail_table = Table(detail_rows, colWidths=[3 * cm, 14 * cm])
    detail_table.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, -1), colors.HexColor("#0d1117")),
        ("TEXTCOLOR", (0, 0), (-1, -1), BODY_TXT),
        ("VALIGN", (0, 0), (-1, -1), "TOP"),
        ("LEFTPADDING", (0, 0), (-1, -1), 8),
        ("RIGHTPADDING", (0, 0), (-1, -1), 8),
        ("TOPPADDING", (0, 0), (-1, -1), 5),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 5),
        ("GRID", (0, 0), (-1, -1), 0.3, colors.HexColor("#222")),
        ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
    ]))
    elements.append(detail_table)

    return elements


